let numero = 10; // Número até o qual queremos imprimir

// Função que imprime os números de 1 até N
function crescentes(N) {
    // Laço que itera de 1 até N
    for (let i = 1; i <= N; i++) {
        console.log(i); // Imprime o número atual
    }
}

// Chama a função com o número definido
crescentes(numero);



