let numero = 10; // Número até o qual queremos imprimir

// Função que imprime os números de N até 1
function decrescentes(N) {
    // Laço que itera de N até 1
    for (let i = N; i >= 1; i--) {
        console.log(i); // Imprime o número atual
    }
}

// Chama a função com o número definido
decrescentes(numero);


